                                <p tabindex="0" class="lead wow animate__fadeInUp">This activity increases students' awareness of how specific elements of physical appearance garner certain impressions, as well as how these impressions are often inaccurate.</p>
                                <p tabindex="0" class="wow animate__fadeInUp">It is important for students to understand that while people are constantly judged on their appearance, it isn't right to do so.  Actions, body, clothing, face, hair, and posture can all play into an impression (albeit an inaccurate one) of someone.  This activity intends to increase students' awareness of this universal experience and to illustrate the inaccuracy of the conclusions many jump to as a result of appearance.  Almost every feature on the form is one an individual has control over, like actions and clothing; body features like height or weight are purposefully omitted.  After taking turns being “judged” while others fill out the form, the person being “judged” goes through the impressions listed by the others and confirms the degree of accuracy of the impressions.</p>
