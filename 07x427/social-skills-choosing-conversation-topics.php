                                <p tabindex="0" class="lead wow animate__fadeInUp">Many individuals (both neurodivergent and neurotypical alike) worry about keeping conversations going and how to select good topics for enthusiastic discussion; this activity demonstrates how to select appropriate, interesting, and relatable topics while emphasizing the vital tenets of good topics and showing how sustaining conversations requires informed choices.</p>
