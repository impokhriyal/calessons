                                <p tabindex="0" class="lead wow animate__fadeInUp">This activity is the natural progression from the Attaining Targeted Impressions activity, as it introduces the feelings of others to the impressions previously addressed.</p>
                                <p tabindex="0" class="wow animate__fadeInUp">In order for a student to begin modifying what they say and the actions they take to obtain a targeted impression, they must first understand the connection between actions, appearances, words, and impressions.  They must also understand that kindness for people (and animals) is not only doing the right thing but is also in their self-interest.  They inevitably will benefit in life from their kindness, as it will give those around them a good impression of them, which can yield comfortable social interactions with friends and strangers alike, friendships, professional dispositions and networking, and romantic relationships.  Given five social interactions, students roll a die for each to see with whom they are interacting in the scenario.  They then answer a handful of questions pertaining to what they can do or say so both they and the other participant can benefit.  It is important to note that in some situations, what benefits the other person is less important, such as during a job interview or an appointment with a doctor.  Through this activity, they will learn to identify the social-emotional needs of others (e.g., the desire to feel important) while acknowledging their own (e.g., how they wish to be perceived).</p>
